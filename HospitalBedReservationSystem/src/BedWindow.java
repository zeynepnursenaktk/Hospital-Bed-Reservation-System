    private void btn_insertBedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertBedActionPerformed
        if (txt_dailyCost.getText().isEmpty() == false) {                                         //Eğer günlük ücret girildiyse if bloğu çalışır.
            try {
                float cost = Float.parseFloat(txt_dailyCost.getText());                           //Ücret string olarak girildiği için floata parse edilir.
                Database.RunCommand("INSERT INTO Bed(type,floor,cost) VALUES('" + cb_bedType.getSelectedItem().toString() + "'," + spinner_floor.getValue().toString() + "," + cost + ")");
                                                                                                  //INSERT ile database e veri ekliyoruz.                
                Database.ShowTable("SELECT id,floor,type,cost FROM Bed", new String[]{"#", "Floor", "Type", "Cost"}, bedsTable, 4);
                                                                                                  //SELECT ile bu eklenen verilen jtable'da gösteriyoruz.
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Invalid number");                            //Geçersiz herhangi bir şey girildiyse de catch bloğu çalışır ve geçersiz uyarısını verir. 
            }
        }

    }//GEN-LAST:event_btn_insertBedActionPerformed

    private void btn_deleteSelectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteSelectedActionPerformed
        try {
            int selection;                                                                       //int tipinde seçim adlı değişken tanımladım.
            selection = Integer.parseInt(bedsTable.getModel().getValueAt(bedsTable.getSelectedRow(), 0).toString()); 
                                                                                                 //bu değişkeni de framemimdeki table'dan çekiyorum.
                                                                                                 //Çektiğim değer integer tipinde olduğu için bununda toStringini alıyorum. 
            Database.RunCommand("DELETE FROM Bed WHERE id=" + selection);                        //Daha sonra database sınıfında yazmış olduğum RunCommand metodumu çağırıp
                                                                                                 //databasede bulunan bu selection ı siliyorum.
            Database.ShowTable("SELECT id,floor,type,cost FROM Bed", new String[]{"#", "Floor", "Type", "Cost"}, bedsTable, 4);
                                                                                                 //ShowTable metodumla da table'dan silinmesini istediğim değerin silindikten sonraki halini ekrana veriyorum.
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Please select a row");                          //Eğer herhangi bir seçim yapılmadıysa da catch bloğum çalışıyor ve lütfen bir satır seç uyarısı veriyor.
        }    }//GEN-LAST:event_btn_deleteSelectedActionPerformed

